package Math;

/**
 *
 * @author htrefftz
 */
public class Point2 {
    /** x coordinate */
    protected double x;
    /** y coordinate */
    protected double y;

    public Point2(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
    
}
